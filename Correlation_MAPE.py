def correl(x, y):
    n = len(x)
    sx = sum(x)
    sy = sum(y)

    sqx = list(x[i] ** 2 for i in range(0, len(x), 1))
    sqy = list(y[i] ** 2 for i in range(0, len(y), 1))
    xy = list(x[i] * y[i] for i in range(0, len(x), 1))

    ssqx = sum(sqx)
    ssqy = sum(sqy)
    sxy = sum(xy)

    Core = ((n * sxy) - (sx * sy)) / (sqrt((n * ssqx) - (sx ** 2)) * sqrt((n * ssqy) - (sy ** 2)))
    return Core

